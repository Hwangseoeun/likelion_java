import java.util.Scanner;

public class Test_1071 {
    //프로그램의 시작점
    public static void main(String[] args) {
        java.util.Scanner scanner = new Scanner(System.in);

        while (true){
            int num = scanner.nextInt();
            if(num==0){
                break;
            }
            System.out.println(num);
        }

    }
}